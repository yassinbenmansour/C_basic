 //Escape Sequence

 #include <stdio.h>

int main ()
{
    printf("hello worl\b\n\\");
    printf("hello worl\b\n\?");
    printf("hello worl\b\n\t");
    printf("hello worl\n\v\r");
    printf("hello worl\n\"");

}

/*
\b	Backspace
\f	Form feed
\n	Newline
\r	Return
\t	Horizontal tab
\v	Vertical tab
\\	Backslash
\'	Single quotation mark
\"	Double quotation mark
\?	Question mark
\0	Null character

*/